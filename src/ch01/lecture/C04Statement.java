package ch01.lecture;

public class C04Statement {
    // `main` : main() Method 자동완성
    public static void main(String[] args) {
        // 명령문
        System.out.println("출력문1");
        // 순서대로 실행됨
        System.out.println("출력문2");
        // ;(세미클론)으로 명령문 마쳐야함.
        System.out.println("출력문3");
        System.out.println("출력문4");
        System.out.println("출력문5");
        // Ctrl + Shift + Enter : 나머지 문장 ;(세미클론) 포함 자동완성
        System.out.print("출력문6");
        // Ctrl + Alt + L : 들여쓰기 자동 (Code Formatting)

        // Ctrl + D : 코드 복사

        // Alt + Shift + 위/아래키 : 코드 위아래로 이동

        // Ctrl + Alt + Enter : 윗 줄에 코드 입력



    }
}
